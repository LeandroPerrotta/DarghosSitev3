<h2>Tenerian movido ao Brasil</h2>
<p>
Caros jogadores,
</p>
<p>
Gostariamos de comunicar que o nosso servidor Tenerian hoje ficou durante algumas horas offline devido a uma transferencia para o nosso servidor no Brasil!
</p>
<p>
Muitos devem estar expantado com esta notícia, mas sim, o Tenerian está novamente funcionando do Brasil. O motivo desta transferencia é que a Empresa que nos fornece o servidor nos comunicou que foi implantado uma nova e eficiente tecnica para a proteção contra os ataques de negação de serviço, e com isto, solicitamos a reativação do servidor.
</p>
<p>
Os ataques testes feitos nos ultimos dias pela equipe da nova solução foram detectados e bloqueados corretamente, tal testes que nos deram confiança em trazer o Darghos novamente ao Brasil. Entretanto não podemos garantir que os ataques não voltem a acontecer, pois o teste que fizemos é significamente inferior em tamanho se comparado com os ataques que sofriamos dos criminosos. Entretanto estamos bastante contentes com os resultados até agora e otimistas.
</p>
<p>
O servidor Elerian continua nos Estados Unidos, e caso Tenerian fique completamente protegida no Brasil estaremos estudando trazer o Elerian ao Brasil tambem em algum tempo.
</p>
<p>
Aos jogadores de Tenerian, tenham um bom jogo! Agora sem nenhum tipo de problema de conexão!
</p>
